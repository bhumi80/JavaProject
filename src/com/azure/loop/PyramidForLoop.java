package com.azure.loop;

public class PyramidForLoop {
    public static void main(String[] args) {
        int num = 10;
        for (int a= 1;a<=num;++a){
            for (int b = 1; b<=a;++b) {
                System.out.print("*");
            }
                System.out.println();
            }
        }
        }



