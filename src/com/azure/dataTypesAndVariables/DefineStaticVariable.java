package com.azure.dataTypesAndVariables;

public class DefineStaticVariable {
    // Static Variable
    static String name = "Anaya";   // nursery child name (static variable define)
    static int age= 3;              // child age

    public static void main(String[] args) {
        String nurseryName = "Bright Horizon";
        System.out.println(nurseryName);
        System.out.println(name + " "  + age + " years old");
    }

}
