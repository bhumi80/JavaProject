package com.azure.operators;

public class ArithmeticOperators {
    public static void main(String[] args) {
        // Arithmetic Operators //

        int a = 35;
        int b = 45;
        System.out.println(" Addition "+" = " + (a + b));    // addition operator

        int c = 60;
        int d = 50;
        System.out.println(" Subtraction " + " = " + (c -d ));    // subtraction operator

        int e = 45;
        int f = 30;
        System.out.println(" Multiplication" + " = " + (e * f));    // multiplication operator

        int g = 55;
        int h = 11;
        System.out.println(" Division" + " = " + (55/11));    // division operator

        int j = 70;
        int k = 40;
        System.out.println(" Modulus " + " = " + (j%k));      // modulus operator

        int l = 34;
        l++;                          // 34+1 = 35
        System.out.println(" Increment value of l++" + " = " + l);        // increment operator

        int m = 26;
        m--;                          // 26-1 = 25
        System.out.println(" Decrement value of m-- " + " = " + m);        // decrement operator



    }
}
