package com.azure.TypeOfDataPrimitive;

public class IntegerInt {
    public static void main(String[] args) {
        int housePrize = 450000;
        int a = 500;
        int b = 1000;
        System.out.println(housePrize);
        System.out.println(a+b);
        int c = 390;
        int d = 670;
        System.out.println(c*d);
        int landPrize = 40000000;
        System.out.println(landPrize);
    }
}
