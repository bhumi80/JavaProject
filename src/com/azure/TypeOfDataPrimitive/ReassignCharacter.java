package com.azure.TypeOfDataPrimitive;
// Reassign Character
public class ReassignCharacter {
    public static void main(String[] args) {
        char a;  // character a declaration
        a = '$';  // character 'a' define as symbol
        System.out.println(a);
        a = '%';  // char reassigned
        System.out.println(a);
    }
}
