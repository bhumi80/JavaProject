package com.azure.TypeOfDataPrimitive;
// Reassign Boolean condition
public class ReassignBoolean {
    public static void main(String[] args) {
        boolean increasePrice;  // boolean declaration
        increasePrice = true;   // assigned condition
        System.out.println(increasePrice);
        increasePrice = false;  // reassigned condition
        System.out.println(increasePrice);

        boolean stockOrder;   // hospital sponge holder order
        stockOrder = true;
        System.out.println(stockOrder);
    }
}
