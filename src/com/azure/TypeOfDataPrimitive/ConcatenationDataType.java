package com.azure.TypeOfDataPrimitive;

public class ConcatenationDataType {
    public static void main(String[] args) {
        String myFirstName = "Bhumika";
        String maidenName = "Patel";
        System.out.println(myFirstName +" " + maidenName);
        String myStreetName = "Welback Road";
        String myPostCode = "HA2 0BT";
        System.out.println(myStreetName + " " + myPostCode);
        String myAge = "40";
        System.out.println("I am "  + myAge + " years old.");
    }
}
