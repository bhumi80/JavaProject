package com.azure.TypeOfDataPrimitive;

public class TypeOfDataBoolean {
    public static void main(String[] args) {
        boolean orderInBulk = true;
        System.out.println(orderInBulk);
        boolean singleInOrder = false;
        System.out.println(singleInOrder);
        boolean buyTicket = false;
        boolean notBuyTicket = true;
        System.out.println(buyTicket);
        System.out.println(notBuyTicket);
        boolean turnRight = false;
        boolean turnLeft = true;
        System.out.println(turnRight);
        System.out.println(turnLeft);
    }
    }

