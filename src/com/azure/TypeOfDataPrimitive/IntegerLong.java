package com.azure.TypeOfDataPrimitive;

public class IntegerLong {
    public static void main(String[] args) {
        long phoneNumber = 8975345667l;
        System.out.println(phoneNumber);
        long winningPrize = 50000000000l;
        System.out.println(winningPrize);
        long lottoPrize = 55000000000000l;
        System.out.println(lottoPrize);
        long earthToMoonDistanceKm = 384400;
        System.out.println(earthToMoonDistanceKm);
        long mobileNumber = 7692341236l;
        System.out.println(mobileNumber);
    }
}
