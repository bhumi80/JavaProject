package com.azure.TypeOfDataPrimitive;

public class IntegerShort {
    public static void main(String[] args) {
        short councilTax = 250;
        short waterBill = 180;
        System.out.println(councilTax);
        System.out.println(waterBill);
        short accountBalance = 23000;
        System.out.println(accountBalance);
        short creditBalance = 1500;
        short carPrize = 12000;
        System.out.println(creditBalance);
        System.out.println(carPrize);
    }
}
