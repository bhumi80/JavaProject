package com.azure.TypeOfDataPrimitive;

public class FloatingDouble {
    public static void main(String[] args) {
        double currency = 56782367.905755d;
        System.out.println(currency);
        double currency1 = 34.789d;
        System.out.println(currency1);
        double balance = 34000.89d;
        System.out.println(balance);
        double lengthCm = 56.780d;
        double heightCm = 45.782d;
        System.out.println(lengthCm + heightCm);
        double currency2 = 845656.890d;
        System.out.println(currency2);
    }
}
