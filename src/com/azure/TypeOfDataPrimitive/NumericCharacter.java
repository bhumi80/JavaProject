package com.azure.TypeOfDataPrimitive;

public class NumericCharacter {
    public static void main(String[] args) {
        char bAlphabet = 'b';
        System.out.println(bAlphabet);
        char symbol1 = '%';
        System.out.println(symbol1);
        char symbol2 = '*';
        System.out.println(symbol2);
        char mAlphabet = 'm';
        System.out.println(mAlphabet);
        char symbol3 = '!';
        System.out.println(symbol3);
    }
}
