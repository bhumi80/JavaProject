package com.azure.TypeOfDataPrimitive;
// Reassign Numeric value
public class ReassignNumericValue {
    public static void main(String[] args) {
        byte waterBill;  //initializing byte
        waterBill = 125;  // declaring value
        System.out.println(waterBill);
        waterBill = 115;  // reassign value
        System.out.println(waterBill);

        short accountCredit; // account credit initializing
        accountCredit = 3456;  // credit value assigned
        System.out.println(accountCredit);
        accountCredit = 2300;  // credit value reassigned
        System.out.println(accountCredit);

        long housePrice;  // house price initializing
        housePrice = 650000; // house price assigned
        System.out.println(housePrice);
        housePrice = 780000;  // reassigned house price
        System.out.println(housePrice);

    }
}
